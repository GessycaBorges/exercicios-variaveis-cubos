// Calculo do perímetro e área de um círculo
const raioCirculo = 1;

const comprimentoCirculo = 2 * Math.PI * raioCirculo;
const areaCirculo = Math.PI * Math.pow(raioCirculo, 2);

console.log (`O comprimento do círculo é ${comprimentoCirculo.toFixed(1)}, e a área do círculo é ${areaCirculo.toFixed(2)}`);